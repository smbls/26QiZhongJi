#include "board.h"
#include "delay.h"
#include "usart.h"
#include "Emm_V5.h"


int main(void)
{
	uint8_t cmd2[8] = {0};

	board_init();

	delay_ms(500);

/**********************************************************
***	ืฐ๏ฟฝุต๏ฟฝ๏ฟฝ2๏ฟฝูถ๏ฟฝฤฃสฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ(Emm_V5_Vel_Control ะญ๏ฟฝ๏ฟฝ)
***	[๏ฟฝ๏ฟฝึท][0xF6][dir][vel_hi][vel_lo][acc][snF][0x6B]
**********************************************************/
	cmd2[0] = 2;						// ๏ฟฝ๏ฟฝ๏ฟฝ2๏ฟฝ๏ฟฝึท
	cmd2[1] = 0xF6;						// ๏ฟฝูถ๏ฟฝฤฃสฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
	cmd2[2] = 0;						// dir: 0=CW
	cmd2[3] = (uint8_t)(1000 >> 8);		// ๏ฟฝูถศธ๏ฟฝ8ฮป
	cmd2[4] = (uint8_t)(1000 & 0xFF);	// ๏ฟฝูถศต๏ฟฝ8ฮป
	cmd2[5] = 100;						// ๏ฟฝ๏ฟฝ๏ฟฝูถ๏ฟฝ
	cmd2[6] = 0;						// snF: 0=๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝึด๏ฟฝ๏ฟฝ
	cmd2[7] = 0x6B;						// ะฃ๏ฟฝ๏ฟฝ๏ฟฝึฝ๏ฟฝ

/**********************************************************
***	หซ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ: อจ๏ฟฝ๏ฟฝUSART1๏ฟฝ๏ฟฝ๏ฟฝอต๏ฟฝ๏ฟฝ1, USART2๏ฟฝ๏ฟฝ๏ฟฝอต๏ฟฝ๏ฟฝ2
***	๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ, สต๏ฟฝึฝ๏ฟฝอฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
**********************************************************/
	Emm_V5_Vel_Control(1, 0, 1000, 100, 0);	// USART1 -> ๏ฟฝ๏ฟฝ๏ฟฝ1
	usart2_SendCmd(cmd2, 8);					// USART2 -> ๏ฟฝ๏ฟฝ๏ฟฝ2

/**********************************************************
***	๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ2ยท๏ฟฝ๏ฟฝำฆ(๏ฟฝษธ๏ฟฝ๏ฟฝ๏ฟฝสต๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝาชัก๏ฟฝ๏ฟฝ๏ฟฝวท๏ฟฝศด๏ฟฝ)
**********************************************************/
	WAIT_RESPONSE();			// ๏ฟฝ๏ฟฝUSART1๏ฟฝ๏ฟฝำฆ
	WAIT_RESPONSE2();			// ๏ฟฝ๏ฟฝUSART2๏ฟฝ๏ฟฝำฆ

	while(1)
	{
	}
}
